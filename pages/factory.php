<h3>Teaching Factory SMK Muhammadiyah Pekalongan</h3>
Teaching Factory SMK Muhammadiyah Pekalongan mulai menggeliat dengan produk unggulan. TF yang akan diungulkan ini muncul dari semua program keahlian yang dimiliki. Semua program keahlian mengunggulkan produk jadi yang dibutuhkan masyarakat, seperti : Pembangkit tenaga surya yang dipelopori oleh Prog. Listrik. Pembuatan penghancur sampah daun dari prog. bodi dan pemesinan, Bengkel surya Otomotif akan menyediakan servis dan ganti oli.
<br /><br />
Dengan Semangat baru dan akan dimualinya kepengurusan serta pegawai baru ini diharapkan akan memberikan semangat baru untuk membuat produk khas dari SMK Muhammadiyah yang dibutuhkan pasaran.
<br /><br />
Pelaksanaan TF tidak lepas dari pekerjaan yang sudah biasa di Unit Poruduksi (UP). UP SMK Muhammadiyah yang telah berjalan 10 tahunan lebih ini semakin mantap dengan diadakannya TF. Dengan TF maka unit produksi di sekolah ini dapat digiatkan semua. Semua bersatu menggerakkan UP yang berada di tiap Program keahlian. Hasil TF yang telah dirasakan oleh sekolah dan siswa adalah banyak diciptakannya berbagai alat dan kreasi dari siswa  dan guru. Sekaligus, alat dan hasil kreasi itu menjadi bagian produk yang dapat dipamerkan serta juga diikutkan dalam ajang lomba di tingkat kota maupun lainnya.

<h3>Teaching Factory "Surya Utama Teknika"</h3>
Teaching Factory <i><b>"Surya Utama Teknika"</b></i> merupakan unit usaha sekolah yang berembrio dari unit produksi setiap kompetensi keahlian. Teaching Factory <i>SMK Muhammadiyah Pekalongan</i> ini telah melayani produk jasa dan pembuatan barang. Produk yang dihasilkan berupa pagar, pintu, tralis jendela, mainan TK serta perbaikan dan cat bodi mobil.
<br /><br />
Pada Tahun ini Teaching Factory <i><b>"Surya Utama Teknika"</b></i> masih mengoptimalkan produk yang banyak dipesan oleh instansi maupun perorangan yaitu:<br /> 
<ol type="1">
	<li>Mesin pencacah sampah untuk pupuk organik</li>
	<li>Mesin pengayak sampah</li>
	<li>Mou dengan Balai Pusat Pengembangan Teknologi (BPPT) pusat dalam pengembangan Mesin Pelorot Batik.</li>
