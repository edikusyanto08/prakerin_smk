<h3>Teknik Instalasi Tenaga Listrik</h3>
Kompetensi kelulusan sebagai teknisi yang berkarakter profesional dalam perencanaan dan pemasangan instalasi rumah tinggal dan gedung bertingkat. Terampil dalam menggunakan alat-alat ukur listrik, memogram dan menginstalasi control industri.
<br /><br />
<b>Keunggulan :</b>
<ol type="1">
    <li>Meluluskan siswa yang siap pakai</li>
    <li>Tenaga pengajar yang berpengalaman</li>
    <li>Praktek industri di bengkel-bengkel yang kompeten</li>
    <li>Ada latihan setir mobil</li>
</ol>
<b>Kompetensi yang diajarkan :</b>
<br />
Membekali siswa dengan pengetahuan, sikap, keterampilan dan religius, agar siswa berkompeten pada :
<ol type="1">
    <li>Pengetahuan alat ukur listrik</li>
    <li>Pengetahuan dan penerapan dasar listrik dan elektronika</li>
    <li>Perawatan dan perbaikan alat-alat rumah tangga</li>
    <li>Perawatan dan perbaikan motor-motor listrik</li>
    <li>Pemasangan, perawatan dan perbaikan instalasi rumah tinggal</li>
    <li>Pemasangan, perawatan dan perbaikan instalasi listrik industri</li>
    <li>Penerapan PLC (Program Logic Control)</li>
</ol>
<b>Prospek lulusan :</b>
<br />
Lulusan program ini dapat diserap di dunia usaha dan industri pada :
<ol type="1">
    <li>Bagian teknisi / maintenance semua jenis industri (baik listik, permesinan maupun otomotif)</li>
    <li>Bengkel-bengkel dynamo dan alat rumah tangga</li>
    <li>Biro Teknik Listrik (BTL)</li>
    <li>Wirausaha dibidang kelistrikan seperti Bengkel Dinamo, reparasi alat rumah tangga dan BTL</li>
</ol>
<b>Lulusan telah diserap di Dunia Usaha dan Dunia Industri :</b>
<br />
PT PLN di Pekalongan, Gardu Induk di Pekalongan, Consuil (Pemeriksan Instalasi), Mitsubishi, Biro Teknik Listrik, di Pabrik Tekstil
